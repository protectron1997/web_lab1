package share;

public class Coordinates {
    private double x=Double.NaN;
    private double r=Double.NaN;
    private Integer y=null;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }

    public Integer getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
